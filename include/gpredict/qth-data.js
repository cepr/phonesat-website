
/**
 * QTH data structure in human readable form.
 * @returns {qth_t}
 * @constructor
 */
function qth_t() {
     this.lat = 0.0;   /*!< Latitude in dec. deg. North. */
     this.lon = 0.0;   /*!< Longitude in dec. deg. East. */
     this.alt = 0;     /*!< Altitude above sea level in meters. */
};
